@pytestmark_live
def test_pg_live_schema_and_crud():
    async def _main():
        from repositories.member_repository import MemberRepository
        from repositories.task_repository import TaskRepository
        db = Database("./unused.db", database_url=PG_DSN)
        await db.connect()
        try:
            # スキーマバージョン
            row = await db.fetchone("SELECT version FROM schema_meta WHERE id = 1")
            assert row["version"] == SCHEMA_VERSION
            # CRUD + lastrowid
            tasks = TaskRepository(db)
            t1 = await tasks.create_task(G1, "PGタスク1", created_by="u1")
            t2 = await tasks.create_task(G1, "PGタスク2", created_by="u1")
            assert t1 and t2 and t2 > t1
            assert (await tasks.get_task(G1, t1))["title"] == "PGタスク1"
            assert await tasks.get_task(G2, t1) is None  # ギルド分離
            # upsert（ON CONFLICT）
            members = MemberRepository(db)
            await members.upsert_team(G1, "wing", "翼")
            await members.upsert_team(G1, "wing", "翼改")
            assert (await members.get_team(G1, "wing"))["team_name"] == "翼改"
            # rowcount
            assert await members.deactivate_team(G1, "wing") is True
            assert await members.deactivate_team(G1, "wing") is False
        finally:
            # 後片付け（テストデータ削除）
            await db.execute("DELETE FROM tasks WHERE guild_id IN (?, ?)", (G1, G2))
            await db.execute("DELETE FROM teams WHERE guild_id IN (?, ?)", (G1, G2))
            await db.close()
    run(_main())


@pytestmark_live
def test_pg_live_sequence_fix():
    """明示 ID 挿入後もシーケンスが修復され PK 衝突しないこと。"""
    async def _main():
        db = Database("./unused.db", database_url=PG_DSN)
        await db.connect()
        try:
            # 明示 ID で大きな値を挿入（SQLite 移行を再現）
            await db.execute(
                "INSERT INTO tasks (local_task_id, guild_id, title, status,"
                " created_by, created_at) VALUES (900001, ?, '明示ID', 'open', 'u1', '2026-01-01')",
                (G1,))
            # シーケンス修復（connect 時にも走るが明示的に再実行）
            await db._pg_fix_sequences()
            # 次の自動採番が衝突しない
            cur = await db.execute(
                "INSERT INTO tasks (guild_id, title, status, created_by, created_at)"
                " VALUES (?, '自動採番', 'open', 'u1', '2026-01-01')", (G1,))
            assert cur.lastrowid > 900001
        finally:
            await db.execute("DELETE FROM tasks WHERE guild_id IN (?, ?)", (G1, G2))
            await db.close()
    run(_main())


