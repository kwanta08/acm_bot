G1 = 100000000000000001
G2 = 200000000000000002


def run(coro):
    return asyncio.run(coro)


# ---------------------------------------------------------------------
# DDL 変換（サーバー不要）
# ---------------------------------------------------------------------
def test_to_pg_ddl_autoincrement_and_guild_id():
    for name, ddl in TABLE_DDL_PG.items():
        # SQLite 固有構文が残っていないこと
        assert "AUTOINCREMENT" not in ddl, name
        assert "datetime('now', 'localtime')" not in ddl, name
        assert "guild_id INTEGER" not in ddl, name
        # PG 側の型が入っていること
        assert "IF NOT EXISTS" in ddl, name
    # IDENTITY 化されること
    assert "BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY" in TABLE_DDL_PG["tasks"]
    # ギルド台帳の PK も BIGINT
    assert "guild_id      BIGINT PRIMARY KEY" in TABLE_DDL_PG["guilds"]
    # 時刻関数の変換
    assert "to_char(CURRENT_TIMESTAMP" in TABLE_DDL_PG["settings"]


def test_to_pg_ddl_preserves_constraints():
    ddl = to_pg_ddl(TABLE_DDL["schedule_votes"])
    assert "UNIQUE (guild_id, option_id, user_id)" in ddl
    assert "ON DELETE CASCADE" in ddl
    assert "CHECK (guild_id >= 0)" in ddl


# ---------------------------------------------------------------------
# プレースホルダ変換（サーバー不要）
# ---------------------------------------------------------------------
def test_prepare_converts_placeholders():
    db = Database("./dummy.db", database_url="postgresql://u:p@localhost/d")
    stmt, args = db._prepare(
        "SELECT * FROM tasks WHERE guild_id = ? AND status = ? AND local_task_id = ?",
        (1, "open", 42))
    assert stmt == ("SELECT * FROM tasks WHERE guild_id = $1"
                    " AND status = $2 AND local_task_id = $3")
    assert args == [1, "open", 42]


def test_prepare_sqlite_passthrough():
    db = Database("./dummy.db")
    stmt, args = db._prepare("SELECT * FROM tasks WHERE guild_id = ?", (1,))
    assert stmt == "SELECT * FROM tasks WHERE guild_id = ?"
    assert args == [1]


def test_driver_name():
    assert Database("./x.db").driver_name == "SQLite"
    assert Database("./x.db", database_url="postgresql://u:p@h/d").driver_name == "PostgreSQL"


